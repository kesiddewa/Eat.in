    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText(); // menerima informasi username yang masuk kedalam jTextFiled1 / username     
        char[] passwordChar = jPasswordField1.getPassword(); // menerima informasi username yang masuk kedalam jPasswordField2 / password
        char[] confirmPasswordChar = jPasswordField2.getPassword();
        String password = convertPassword(passwordChar);
        String confirmPassword = convertPassword(confirmPasswordChar);
        
        if (username.equals("") || password.equals("")){ // check apakah field kosong atau tidak
            JOptionPane.showMessageDialog(rootPane, "tidak boleh kosong", "error", 1);
        } else {
            if (password.equalsIgnoreCase(confirmPassword)){
                try {
                    prt = conn.prepareStatement("select * from login where username=?"); 
                    prt.setString(1, username);
                    rst = prt.executeQuery();

                    if (!rst.next()){
                        int id = (int)(Math.random() * (99999999 - 11111111 + 1) + 11111111);
                        prt = conn.prepareStatement("insert into login values (?, ?, ?, 'customer')");
                        prt.setInt(1, id);
                        prt.setString(2, username);
                        prt.setString(3, password);
                        prt.executeUpdate();
                        JOptionPane.showMessageDialog(rootPane, "akun anda berhasil dibuat", "success", 1);
                        new LoginPage().setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "username telah terpakai", "error", 1);
                    }
                } catch (Exception e){
                    e.printStackTrace();
                }
            } else {

            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

